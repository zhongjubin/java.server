package com.ssh.beans;

public class Agv {

	private String msg;
	private int xcoord;
	private int ycoord;
	private String netid;
	private int agvId;
	
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public int getXcoord() {
		return xcoord;
	}
	public void setXcoord(int xcoord) {
		this.xcoord = xcoord;
	}
	public int getYcoord() {
		return ycoord;
	}
	public void setYcoord(int ycoord) {
		this.ycoord = ycoord;
	}
	public String getNetid() {
		return netid;
	}
	public void setNetid(String netid) {
		this.netid = netid;
	}
	public int getAgvId() {
		return agvId;
	}
	public void setAgvId(int agvId) {
		this.agvId = agvId;
	}
	

}